/* Overlay Layers */
var lights = L.geoJson(null, {
  pointToLayer: function (feature, latlng) {
    return L.marker(latlng, {
      icon: L.icon({
        iconUrl: "assets/img/lightsfullarrow.png",
        iconSize: [24, 28],
        iconAnchor: [12, 28],
        popupAnchor: [0, -25]
      }),
      title: "Semáforo",
      riseOnHover: true
    });
  },
  onEachFeature: function (feature, layer) {
    if (feature.properties) {
      var content = "<table class='table table-striped table-bordered table-condensed'>" + "<tr><th>Local:</th><td>" + feature.properties.local + "</td></tr>" +  + "<table>";

      if (document.body.clientWidth <= 767) {
        layer.on({
          click: function (e) {
            $("#feature-title").html("Semáforo");
            $("#feature-info").html(content);
            $("#featureModal").modal("show");
          }
        });

      } else {
        layer.bindPopup(content, {
          maxWidth: "auto",
          closeButton: false
        });
      }
      theaterSearch.push({
        name: layer.feature.properties.local,
        source: "Theaters",
        id: L.stamp(layer),
        lat: layer.feature.geometry.coordinates[1],
        lng: layer.feature.geometry.coordinates[0]
      });
    }
  }
});
$.getJSON("data/lights.json", function (data) {
	var i;
	var geodata = {
		type: "FeatureCollection",
		features: []
	};
	
	for(i=0; i<200; i++)
	{
		var feat = {
			type: "Feature",
			id: data[i].id,
			properties: {
					local: data[i].local
				},
			geometry: {
				type: "Point",
				coordinates: [ data[i].longitude, data[i].latitude ]
			}
		};
		geodata.features.push(feat);
	}
	
  lights.addData(geodata);
});


